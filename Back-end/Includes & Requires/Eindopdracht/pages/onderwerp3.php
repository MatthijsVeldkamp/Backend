<h5>Minecraft is een sandbox-game ontwikkeld door Mojang Studios, oorspronkelijk uitgebracht in 2009 en volledig uitgebracht in 2011. Het is een van de bestverkopende videogames aller tijden, met meer dan 300 miljoen verkochte exemplaren en bijna 140 miljoen maandelijkse actieve spelers in 2023. Het spel staat bekend om zijn vrijheid en creativiteit, waarbij spelers in een blockmatig, drie-dimensionaal, proceduraal gegenereerd wereld kunnen verkennen, bouwen, vechten en samenwerken.</h5>