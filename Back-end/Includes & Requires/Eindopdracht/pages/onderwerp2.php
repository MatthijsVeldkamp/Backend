<h2>Arch Linux</h2>
<h5>Arch Linux is een onafhankelijk ontwikkelde, x86-64 algemene GNU/Linux-distributie die streeft naar het leveren van de nieuwste stabiele versies van de meeste software door een rolling-release model te volgen. Het standaardinstallatiepakket is een minimalistisch basis systeem, geconfigureerd door de gebruiker om alleen wat opzettelijk nodig is toe te voegen. Arch Linux is gericht op de bekwame GNU/Linux-gebruiker of iedereen met een do-it-yourself-houding die bereid is om de documentatie te lezen en hun eigen problemen op te lossen.</h5>